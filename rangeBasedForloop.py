# lecture 13: Range-based For Loop

for i in range(9):  # range(9) means loop start from 0-8, 9 is explicit no. means aren't included
    print(i)

for j in range(1, 11):  # range(1,11) means loop starts from 1 and ends with 10, becoz 11 is explicit no.
    print(j)

for k in range(1, 11, 2):   # here, 3rd parameter used to remove the next alternate if value is 2 means it will remove 1 next alternate no
    print(k)
